# Operadores Aritméticos em Python

# O que são operadores aritméticos?
# São operadores que realizam operações matemáticas básicas, como adição, subtração, multiplicação, divisão, etc.

# Soma (+)
# Exemplo: 5 + 3 = 8

# Subtração (-)
# Exemplo: 10 - 5 = 5

# Multiplicação (*)
# Exemplo: 3 * 2 = 6

# Divisão (/)    
# Exemplo: 10 / 2 = 5.0

# Divisão Inteira (//)
# Exemplo: 10 // 3 = 3

# Resto da Divisão (%)
# Exemplo: 10 % 3 = 1

# Exponenciação (**)
# Exemplo: 2 ** 3 = 8   
# Ordem de Precedência
# 1. ()
# 2. **
# 3. *, /, //, %
# 4. +, -   
# Exemplos:
# 5 + 3 * 2 = 11
# (5 + 3) * 2 = 16
# 3 * 2 + 5 = 7
# 3 * (2 + 5) = 15
# 3 * 2 ** 2 = 18
# 3 ** 2 * 2 = 24
# 3 ** (2 * 2) = 512
# 3 * 2 / 4 = 1.5
# 3 / 4 * 2 = 0.75
# 3 // 4 * 2 = 1
# 3 % 4 * 2 = 2
# 3 * 2 % 4 = 0
# 3 * (2 % 4) = 0