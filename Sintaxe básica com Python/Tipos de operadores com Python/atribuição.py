# Operadores de Atribuição em Python

# O que é atribuição?
# Atribuição é o processo de atribuir um valor a uma variável. Em Python, o sinal de igual (=) é usado para atribuição.

# Exemplos:



# Atribuição simples
x = 5
print(x)  # Output: 5

# Atribuição múltipla
x, y, z = 10, 20, 30
print(x, y, z)  # Output: 10 20 30

# Exemplos de operadores de atribuição

# Atribuição aditiva
x = 5
x += 10  # x = x + 10
print(x)  # Output: 15

# Atribuição subtrativa
x = 5
x -= 10  # x = x - 10
print(x)  # Output: -5

# Atribuição multiplicativa
x = 5
x *= 10  # x = x * 10
print(x)  # Output: 50

# Atribuição divisiva
x = 50
x /= 10  # x = x / 10
print(x)  # Output: 5.0

# Atribuição modular
x = 50
x %= 10  # x = x % 10
print(x)  # Output: 0

