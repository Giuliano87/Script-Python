# Operadores Logicos em Python

# O que eles fazem?
# Os operadores logicos em Python servem para realizar operacoes lógicas, ou seja, verificar se uma condição é verdadeira ou falsa.

# Operadores Lógicos em Python
# Os operadores lógicos em Python são:  
# - and (e)
# - or (ou)
# - not (não)

# Exemplos  
# 1. and (e)
# O operador and (e) é usado para verificar se duas condições são verdadeiras.

# Exemplo:
# x = 5
# y = 10
# if x > 0 and y > 0:
#     print("x e y são positivos")

# 2. or (ou)    
# O operador or (ou) é usado para verificar se uma das condições é verdadeira.  

# Exemplo:
# x = 5
# y = 0
# if x > 0 or y > 0:
#     print("x ou y é positivo")

