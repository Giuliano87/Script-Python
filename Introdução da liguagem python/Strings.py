# O que são strings em Python?
# Strings em Python são sequências de caracteres que são representadas por aspas simples ou duplas.
# Exemplos de strings em Python:

# Exemplo 1:
string1 = "Hello, World!"
print(string1)

# Exemplo 2:    
string2 = 'Python é uma linguagem de programação'
print(string2)  

# Exemplo 3:
string3 = "12345"
print(string3)

# Exemplo 4:
string4 = '12345'
print(string4)

# Comentários:
# - Strings em Python são imutáveis, isto é, elas não podem ser alteradas depois de serem criadas.
# - Strings em Python são representadas por aspas simples ou duplas.
# - Strings em Python podem ser delimitadas por aspas simples (') ou duplas (").
# - Strings em Python podem conter caracteres alfanuméricos, espaços, acentos, símbolos, etc.
# - Strings em Python podem ser indexadas, isto é, você pode acessar um caractere específico de uma string usando sua posição.