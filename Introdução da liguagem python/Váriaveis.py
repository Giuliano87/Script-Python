# Variáveis em python são declaradas com o uso de um símbolo de igual (=). 
# Exemplos:
# Declarando uma variável do tipo inteiro
num = 10

# Declarando uma variável do tipo string
nome = "John"

# Declarando uma variável do tipo float
pi = 3.14159

# Podemos imprimir o valor de uma variável usando a função print()
print(num)
print(nome)
print(pi)   

# Podemos alterar o valor de uma variável usando a atribuição ( = )
num = 20
nome = "Jane"
pi = 3.1416

# Podemos imprimir o novo valor de uma variável
print(num)
print(nome)




